public class HelloEx003{
    public static void main(String[] args){
        int x = 10;
        int y = 20;
        System.out.println("x + y = " + (x+y));// 더하기
        System.out.println("x - y = " + (x-y));// 빼기
        System.out.println("x * y = " + (x*y));// 곱하기
        System.out.println("x / y = " + (x/y));// 나누기
    }
}